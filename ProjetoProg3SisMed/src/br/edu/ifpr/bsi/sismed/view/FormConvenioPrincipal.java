    private void atualizaTabela() {

         try {
             listaConvenios = ConvenioDAO.pesquisar(retornaObjeto());
        
         DefaultTableModel modelo = (DefaultTableModel) jTBConvenio.getModel();
         modelo.setNumRows(0);
         for (Convenio convenio : listaConvenios) {
             modelo.addRow(new Object[]{
                 convenio.getIdConvenio(),
                 convenio.getNomeConvenio()
                 
             });
         }
          } catch (SQLException ex) {
             System.out.println("Dados não disponíveis.");
         }
    }

    private Convenio retornaObjeto() {
        Convenio e = new Convenio();
        e.setNomeConvenio(jTFConvenio.getText().toUpperCase());
        return e;
    }
